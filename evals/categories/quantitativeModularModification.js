/**
 * This is the quantitative modular modification test
 * 
 * The quantitative modular modification evaluation category tests whether LLMs are able to modify
 * existing stock-and-flow models and use the causal relationships and key processes present to create
 * new models at the user's request. Tested abilities include the ability of LLMs to create a modular
 * version of a non-modular model and create a non-modular model from a model where modules are present,
 * and the ability to add and delete relationships and modules in a provided model with modules.
 * 
 * The evaluation builds upon the quantitative modular reasoning evaluation. A base stock-and-flow model
 * is passed to the LLM via a JSON format (models can be found in evals/categories/modularModificationData), 
 * and an additional prompt is passed to the LLM with the requested changes. The generated model is checked
 * against a list of expectations to determine the success of the LLM, using the evaluation standard 
 * defined in the quantitative modular reasoning evaluation.
 * 
 * @module categories/quantitativeModularModification
 */

import fs from 'fs';
import path from 'path';
